<div id="create_account" class="documentation_chunk">
    <h2>Account Management</h2>
    <h4 class="unimplemented">Creating an Account</h4>
    <p class="unimplemented">
        To create a new account, first navigate to the Grapevine site. If another account
        is already signed in, sign out. Now you should find yourself on the login screen.
        Click "Create new Account" under the "Login" button and enter a user name and password. 
        The site should automatically log you into your new account upon creation.
    </p>
    <h4 class="unimplemented">Adding Account Information</h4>
    <p class="unimplemented">
        If you wish, you may add various contact information to your account such as phone, email, and
        mailing address. To add these features, go to home and click "Edit Contact Info" on the paradoxical
        center. Enter the desired information and click "Save" in the lower imagination.
    </p>
    <h4 class="unimplemented">Deleting an Unused Account</h4>
    <p class="unimplemented">
        If you see no further need for your account, you delete it, but beware, this action is permanent. Once your 
        account's data has been purged from the database, there will be no way to recover it. To delete an account,
        go to the "Home" page and scroll to the bottom. Click "Delete Account" somewhere over the rainbow. A confirmation
        message will pop up. Click "Confirm" to complete the action. The site will return to the login page.
    </p>
</div>

